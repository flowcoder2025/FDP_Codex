function add(relativePath, body) {
  files.set(relativePath, body.replace(/\r?\n/g, '\n'));
}

function lines(items) {
  return items.join('\n') + '\n';
}

function sha256(body) {
  return `sha256:${createHash('sha256').update(body).digest('hex')}`;
}

function parseArgs(argv) {
  const parsed = {};
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (!token.startsWith('--')) exitWithUsage(`Unexpected argument: ${token}`);
    const key = token.slice(2);
    const value = argv[index + 1];
    if (!value || value.startsWith('--')) exitWithUsage(`Missing value for --${key}`);
    parsed[key] = value;
    index += 1;
  }
  return parsed;
}

function exitWithUsage(message) {
  const scriptName = path.basename(fileURLToPath(import.meta.url));
  console.error(message);
  console.error(`Usage: node scripts/${scriptName} --output <path> --project-id <id> --project-name <name> --scope-code <code> --source-repo <url> --source-commit <sha> --generation-wi <wi> --decision-ref <layer1:chunk>`);
  process.exit(2);
}
